-> Diferente da estrutura if/else, o switch/case avalia apenas condições de igualdade. 
Ou seja, ele verifica se o valor passado como parâmetro é igual a alguma das condições especificadas em seu corpo.
Esse comportamento equivale a ultilização de vários if/elses em sequência, porém com uma sintaxe mais enxulta e de fácil leitura.


*/

